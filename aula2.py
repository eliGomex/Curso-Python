#Função print exibe algo na tela, recebe vários argumentos
#usa-se virgulas, texto usa-se entre aspas simples ou duplas
print(12, 34, sep= "-") #sep adiciona um separador, aspas duplas
print(30, 204, sep= ' e ')#sep adiciona um separador, aspas simples
print('Texto entre aspas simples')
# \r\n -> CRLF - quebra linha, vem padrão do windowa
print(23, 22, 60, sep='separador', end='quebra linha')
print(10, 15, 69, sep='-', end='\n')
print(3, 2, 5, sep='-', end='\n')


